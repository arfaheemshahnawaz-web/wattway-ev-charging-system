function routeToStation(destLat,destLng,button){
    if(!userMarker){ alert('Locate yourself first!'); return; }
    const startLat = userMarker.getLatLng().lat;
    const startLng = userMarker.getLatLng().lng;
    if(routingControl){ driverMap.removeControl(routingControl); routingControl=null; button.textContent='Route & Info'; return; }
    routingControl = L.Routing.control({
        waypoints: [L.latLng(startLat,startLng), L.latLng(destLat,destLng)],
        routeWhileDragging: true,
        fitSelectedRoutes: true,
        lineOptions: { styles: [{color: '#4CAF50', weight:5}] },
        createMarker: ()=>null,
        router: L.Routing.osrmv1({ serviceUrl: 'https://router.project-osrm.org/route/v1' })
    }).addTo(driverMap);
    routingControl.on('routesfound', e=>{
        const route = e.routes[0];
        const distanceKm = (route.summary.totalDistance/1000).toFixed(2);
        const timeMin = Math.ceil(route.summary.totalTime/60);
        // Find nearest sibling .distance-time element after the button
        const parentCard = button.closest('.card');
        const distanceEl = parentCard ? parentCard.querySelector('.distance-time') : null;
        if (distanceEl) distanceEl.textContent = `Distance: ${distanceKm} km | ETA: ${timeMin} min`;
    });
}
