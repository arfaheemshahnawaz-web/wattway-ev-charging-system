            }
        }, ()=> alert('Could not get your location'), { enableHighAccuracy:true, maximumAge:0, timeout:5000 });
    } else alert('Geolocation not supported');
});

function updateNearbyStations(lat,lng){
    const nearby = ALL_STATIONS.filter(station=>{
        const d = getDistanceFromLatLon(lat,lng, parseFloat(station.latitude), parseFloat(station.longitude));
        return d <= 15;
    });
    plotMarkers(nearby);
}

