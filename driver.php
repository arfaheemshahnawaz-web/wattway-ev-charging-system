// Fetch available slots
let timePicker = null;
function fetchAvailableSlots(stationId, date){
    axios.get(`api/get_slot.php?station_id=${stationId}&date=${date}`)
    .then(res=>{
        const times = res.data
            .filter(slot => String(slot.is_available).toLowerCase() === 'yes')
            .map(slot => slot.slot_time);

        if(times.length){
            document.getElementById('slotMessage').textContent = '';
            if(timePicker) timePicker.destroy();

            timePicker = flatpickr("#visit_time", {
                enableTime: true,
                noCalendar: true,
                dateFormat: "H:i",
                time_24hr: true,
                enable: times,
                defaultHour: parseInt(times[0].split(":")[0]),
                defaultMinute: parseInt(times[0].split(":")[1])
            });

        } else {
            document.getElementById('slotMessage').textContent = 'No available slots for this date.';
            if(timePicker) timePicker.clear();
        }
    })
    .catch(err=>{
        console.error(err);
        document.getElementById('slotMessage').textContent = 'Failed to fetch slots.';
        if(timePicker) timePicker.clear();
    });
}

